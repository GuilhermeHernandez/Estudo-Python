'''

Operadores Aritmeticos

Ordem de precedencia dos operadores : () , ** , * Ou / , + Ou -

'''

a = 10
b = 3

resultado_soma = a + b 
resultado_sub = a - b
resultado_div = a / b
resultado_multi = a * b
resultado_resto = a % b
resultado_potencia = a ** b

print(resultado_soma)
print(resultado_sub)
print(resultado_div)
print(resultado_multi)
print(resultado_resto)
print(resultado_potencia)