'''

Funções internas
    - (+,-,*,/,**,//)
    - Max(list) -> Maior valor de uma lista
    - Min(list) -> Menor valor de uma lista
    - abs(valor) -> Retorna o valor absoluto
    - pow(base,expoente) -> Retonar o valor da potencia
    - round(valor,casas) -> Retorna o valor de arredondamento

'''

valores = [2,5,8,-1,0,11,7,-6]

# Maior valor de um lista de valores
print(max(valores))

# Menor valor de uma lista de valores
print(min(valores))

# Como saber o valor absoluto (ignora sinal)
a = -5 
b = 4 

print(abs(a))

# Como saber a potencia
print(pow(a,b))

# Como fazer arrendodamento
c = 2.789011

print(round(c,2))